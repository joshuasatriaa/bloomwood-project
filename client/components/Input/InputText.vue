<template>
  <div
    class="relative"
    :class="[
      { '-translate-y-7': variant === 'underlined' },
      variant === 'outlined'
        ? 'input-field-outlined'
        : 'input-field-underlined',
    ]"
  >
    <input
      :id="id"
      v-bind="$attrs"
      ref="formInput"
      placeholder=" "
      :type="type"
      :value="value"
      required
      class="w-full bg-transparent border-primary focus:outline-none c-input"
      :class="[
        {
          'mt-5 border-b-4': variant === 'underlined',
          'border-2 rounded-sm ': variant === 'outlined',
          'h-10': height === 'default',
          'h-8': height === 'short',
          'text-sm': fontSize === 'small',
        },
        getBackgroundColor,
      ]"
      @input="onInput($event)"
    />
    <label
      :for="id"
      class="
        transition
        transform
        duration-800
        cursor-text
        ease-in-out
        left-0
        absolute
        bg-transparent
        text-secondary
        font-bold
      "
      :class="[
        {
          '-translate-y-5': value !== '' && variant === 'underlined',
          '-translate-y-8': value !== '' && variant === 'outlined',
          'translate-x-5': variant === 'outlined' && value === '',
        },
        variant !== 'outlined' ? 'bottom-3' : 'bottom-2',
      ]"
    >
      {{ label }}
    </label>
  </div>
</template>
<script>
export default {
  name: 'InputText',
  props: {
    id: {
      type: String,
      required: true,
    },
    label: {
      type: String,
      required: true,
    },
    value: {
      type: String,
      default: '',
    },
    type: {
      type: String,
      default: 'text',
    },
    height: {
      type: String,
      default: 'default',
      validator(value) {
        return ['short', 'default'].includes(value)
      },
    },
    variant: {
      type: String,
      default: 'underlined',
      validator(value) {
        return ['underlined', 'outlined'].includes(value)
      },
    },
    backgroundColor: {
      type: String,
      default: 'transparent',
    },
  },
  computed: {
    getBackgroundColor() {
      return `bg-${this.backgroundColor}`
    },
  },
  methods: {
    onInput(event) {
      this.$emit('input', event.target.value)
    },
  },
}
</script>
<style>
.input-field-underlined:focus-within label {
  @apply -translate-y-5;
}

.input-field-outlined:focus-within label {
  @apply -translate-y-8;
  @apply -translate-x-0;
}

.c-input:not(:placeholder-shown) ~ label {
  @apply -translate-y-5;
}
</style>
